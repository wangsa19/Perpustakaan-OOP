    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        // Ambil data anggota dan buku
        String nmAnggota = tfNamaAnggota.getText();
        String bukuDipilih = (String) cbBuku.getSelectedItem(); // Casting untuk mendapatkan objek Buku

        // Misalkan tanggal peminjaman adalah hari ini dan tanggal kembali 7 hari ke depan
        String tanggalPinjam = LocalDate.now().toString(); // Menggunakan LocalDate untuk mendapatkan tanggal saat ini
        String tanggalKembali = LocalDate.now().plusDays(7).toString(); // Menambahkan 7 hari

        if (bukuDipilih != null && nmAnggota != null && !nmAnggota.isEmpty()) {
            // Cari anggota berdasarkan nama
            Anggota anggota = perpus.cariAnggota(nmAnggota);
//            System.out.println("Nama Anggota: " + nmAnggota);

            if (anggota != null) {
                Buku buku = perpus.cariBuku(bukuDipilih);
                System.out.println("Buku Dipilih: " + buku);
                if (buku == null) {
                    JOptionPane.showMessageDialog(this, "Buku tidak ditemukan!");
                } else {
                    Peminjaman peminjaman = new Peminjaman(buku, anggota, tanggalPinjam, tanggalKembali);
                    perpus.tambahPeminjaman(peminjaman); // Simpan peminjaman ke Perpustakaan
                    perpus.simpanPeminjamanKeFile();
                    JOptionPane.showMessageDialog(this, "Buku berhasil dipinjam!");
                    // Opsional: Bersihkan input setelah peminjaman
                    tfNamaAnggota.setText("");
                    cbBuku.setSelectedIndex(-1); // Reset combo box
                }
            } else {
                JOptionPane.showMessageDialog(this, "Anggota tidak ditemukan!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data anggota atau buku tidak valid!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

