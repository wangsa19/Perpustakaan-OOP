    private List<String> bacaBukuDariFile(String fileName) {
        List<String> daftarBuku = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = br.readLine()) != null) {
                // Asumsikan setiap baris adalah judul buku
                String[] dataBuku = line.split(","); // Pisahkan berdasarkan koma
                if (dataBuku.length > 0) {
                    daftarBuku.add(dataBuku[0]); // Menambahkan judul buku ke daftar
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        return daftarBuku;
    }

// Di dalam metode inisialisasi atau constructor Anda
    public void inisialisasiComboBox() {
        List<String> bukuList = bacaBukuDariFile("buku.txt");
        for (String buku : bukuList) {
            cbBuku.addItem(buku); // Mengisi ComboBox dengan daftar buku
        }
    }
    
    private ArrayList<Buku> loadBukuFromFile(String filePath) {
        ArrayList<Buku> bukuList = new ArrayList<>();
        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("\\,"); // Memisahkan berdasarkan pemisah '|'
                if (parts.length == 5) {
//                    int id = Integer.parseInt(parts[0]);
                    String judul = parts[0];
                    String pengarang = parts[1];
                    String isbn = parts[2];
                    int tahunTerbit = Integer.parseInt(parts[3]);
                    boolean tersedia = Boolean.parseBoolean(parts[4]);
                    bukuList.add(new Buku(judul, pengarang, isbn, tahunTerbit, tersedia));
                    System.out.println("Buku dimuat: " + judul);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return bukuList;
    }
    

